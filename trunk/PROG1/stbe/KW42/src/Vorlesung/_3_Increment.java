/** 
 * increments the value of a variable by 1
 */
public class _3_Increment {
   public static void main(String[] args) {
       int counter = 0;
       System.out.println("Anfangswert: " + counter);
       counter = counter + 1;
       System.out.println("Wert nach Erhoehung: " + counter);
   }
}
